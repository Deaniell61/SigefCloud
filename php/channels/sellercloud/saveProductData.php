function prodExistsInChannel($sku, $channel, $country){
    $query = "
        SELECT 
            pro.ESTATUS
        FROM
            tra_cha_pro AS pro
                INNER JOIN
            cat_sal_cha AS cha ON pro.codchan = cha.codchan
        WHERE
            cha.channel = '$channel'
                AND (pro.mastersku = '$sku'
                OR pro.channelsku = '$sku');
    ";

    $result = mysqli_query(conexion($country), $query);

    $response = 0;

    if($result->num_rows > 0){
        if(mysqli_fetch_array($result)[0] == "ENABLED"){
            $response = 1;
        }
    }

    return $response;
}

function calcDim($qty, $ancho, $profun){
    $cont = 1;
    do{
        $cont ++;
        $sqr = $cont * $cont;
    }while($qty > $sqr);

    $row = ceil($qty / $cont);

    $ancho = $ancho * $cont;
    $profun = $profun * $row;

    return [$ancho, $profun];
}